    <h1>Estructuras repetitivas</h1>
    <p>Las estructuras repetitivas en Java son bucles que permiten ejecutar un bloque de código varias veces. Las más comunes son:
        - for: se utiliza cuando se conoce el número exacto de iteraciones.
        - while: se utiliza cuando no se conoce el número exacto de iteraciones y depende de una condición.
        - do-while: similar a while, pero garantiza que el bloque de código se ejecute al menos una vez.
    <h1>POO</h1>
    <p>La Programación Orientada a Objetos (POO) es un paradigma de programación que utiliza "objetos" para representar datos y métodos. Los conceptos clave de la POO incluyen:
        - Clases: plantillas para crear objetos.
        - Objetos: instancias de clases que contienen atributos y métodos.
        - Herencia: permite que una clase herede atributos y métodos de otra clase.
    <h1>Herencia</h1>
    <p>La herencia es un concepto fundamental en la Programación Orientada a Objetos (POO) que permite a una clase (subclase) heredar atributos y métodos de otra clase (superclase). Esto promueve la reutilización del código y establece una relación jerárquica entre las clases. Por ejemplo, si tenemos una clase "Animal" con atributos y métodos comunes, podemos crear subclases como "Perro" y "Gato" que hereden esas características y agreguen sus propias particularidades.

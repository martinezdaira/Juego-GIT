    <h1>Sintaxis Java</h1>
    <P>La sintaxis de Java establece reglas para escribir programas, usando punto y coma para finalizar instrucciones y llaves para definir bloques. Todo programa está compuesto por clases y comienza su ejecución en el método public static void main(String[] args). Las variables deben declararse indicando su tipo y nombre, y los objetos se crean con la sintaxis NombreClase nombreObjeto = new NombreClase();. </P>
    <h1>Estructura repetitivas</h1>
    <p> Son bloques de código que permitan ejecutar un conjunto de instrucciones múltiples veces hasta que se cumpla una condicion específica.</p>
    <h1>POO</h1>
    <p>La Programación Orientada a Objetos (POO) es un paradigma de programación que organiza el código en objetos, que son entidades con datos (atributos) y comportamientos (métodos) asociados.</p>
    <h1>Herencia</h1>
    <p>concepto fundamental de la programación orientada a objetos que permite a una nueva clase (clase derivada) heredar atributos y métodos de una clase existente (clase base o padre)</p>

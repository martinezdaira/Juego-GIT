<p>En JavasScript las estructuras repetitivos permiten repetir un código varias , las más usadas son:for,while, y do-while</p>
<h1>POO (atributos y métodos)</h1>
<p>POO, significa <strong>programación orientada a objetos</strong> que organiza el código en objetos, los cuales combinan datos (atributos o propiedades) y comportamientos (métodos o funciones)</p>
<h2>ATRIBUTOS</h2>
<p>son las características que hay dentro, por ejemplo: 
    -Uniforme
    -Comportamiento
    -Horarios
</p>
<h2>MÉTODOS</h2>
<p>son las acciones que puede realizar un objeto, por ejemplo:
    -Estudiar
    -Jugar
    -Ayudar en casa
    -Dar cariño
    </p>
<h1>HERENCIA </h1>
<p>La herencia permite a las subclases heredar atributos y métodos de la superclase, lo que significa que la subclase puede acceder y utilizar características y comportamientos definidos de la superclase.</p>

